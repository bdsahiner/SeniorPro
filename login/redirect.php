    <script>
    // Function to redirect after countdown
    function redirectWithCountdown(url, seconds) {
        // Update countdown every second
        var countdownElement = document.getElementById('countdown');
        var countdownInterval = setInterval(function() {
            seconds--;
            countdownElement.textContent = seconds;
            // If countdown reaches 0, redirect and clear the interval
            if (seconds <= 0) {
                clearInterval(countdownInterval);
                window.location.href = url;
            }
        }, 1000);
    }

    // Call the function with the URL and countdown duration
    redirectWithCountdown('login', 5);
    </script>
